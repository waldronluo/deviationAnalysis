function labelNum = MCLabel (label)
    labelNum = 0;
    switch (label)
    case 'a' labelNum = 1;
    case 'i' labelNum = 2;
    case 'd' labelNum = 3;
    case 'k' labelNum = 4;
    case 'pc' labelNum = 5;
    case 'nc' labelNum = 6;
    case 'c' labelNum = 7;
    case 'u' labelNum = 8;
    case 'n' labelNum = 9;
    case 'z' labelNum = 10;

    end

end
